package com.GDJ32.vo;


// DB 테이블 정보를 저장해서 테스트 하기 위한 클래스
public class TestVo {
	private Integer no;
	private String id;
	private String name;
	
	public Integer getNo() {
		return no;
	}
	public void setNo(Integer no) {
		this.no = no;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
